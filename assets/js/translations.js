const KatiaI18n = {
  currentLang: localStorage.getItem('katia-lang') || 'es',
  init() { this.applyTranslations(this.currentLang); this.setupToggle(); },
  applyTranslations(lang) {
    this.currentLang = lang;
    localStorage.setItem('katia-lang', lang);
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const translation = translations[lang][key];
      if (translation !== undefined) el.textContent = translation;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const translation = translations[lang][key];
      if (translation !== undefined) el.placeholder = translation;
    });
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });
  },
  setupToggle() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', () => this.applyTranslations(btn.dataset.lang));
    });
  },
  t(key) { return translations[this.currentLang][key] || key; }
};

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => KatiaI18n.init());
} else {
  KatiaI18n.init();
}
